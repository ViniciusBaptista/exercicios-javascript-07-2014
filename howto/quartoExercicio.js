/**
 * Crie um objeto chamado Calc que implemente as quatro operações
 * básicas da matemática (soma, subtração, multiplicação e divisão).
 */
 
var Calc = {
    soma: function(a, b) {
      return a + b;
    },
    subtracao: function(a, b) {
      return a - b;
    },
    multiplicacao: function(a, b) {
      return a * b;
    },
    divisao: function(a, b) {
      return a / b;
    }
};
